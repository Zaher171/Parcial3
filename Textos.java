/**
 * Clase utilitaria que contiene todos los textos fijos usados en pantalla.
 */
public class Textos {
    /** Prefijo para la representación textual de un empleado */
    public static final String EMPLEADO_PREFIX = "Empleado{";
    /** Texto para el nombre */
    public static final String NOMBRE = "nombre='";
    /** Texto para el cargo */
    public static final String CARGO = ", cargo='";
    /** Texto para el salario */
    public static final String SALARIO = ", salario=";
    /** Sufijo para la representación textual de un empleado */
    public static final String SUFFIX = "'}";
    /** Mensaje mostrado tras aumentar el salario */
    public static final String TRAS_AUMENTO = "\nTras aumento de salario:";
}